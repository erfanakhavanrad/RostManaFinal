package com.example.rostmanafinal.Pojo;

public class DaysOfWeek {
    private String shanbe;
    private String yekshanbe;
    private String doshanbe;
    private String seshanbe;
    private String chaharshanbe;
    private String panjshanbe;
    private String jome;

    public String getShanbe() {
        return shanbe;
    }

    public String getYekshanbe() {
        return yekshanbe;
    }

    public String getDoshanbe() {
        return doshanbe;
    }

    public String getSeshanbe() {
        return seshanbe;
    }

    public String getChaharshanbe() {
        return chaharshanbe;
    }

    public String getPanjshanbe() {
        return panjshanbe;
    }

    public String getJome() {
        return jome;
    }

    public void setShanbe(String shanbe) {
        this.shanbe = shanbe;
    }

    public void setYekshanbe(String yekshanbe) {
        this.yekshanbe = yekshanbe;
    }

    public void setDoshanbe(String doshanbe) {
        this.doshanbe = doshanbe;
    }

    public void setSeshanbe(String seshanbe) {
        this.seshanbe = seshanbe;
    }

    public void setChaharshanbe(String chaharshanbe) {
        this.chaharshanbe = chaharshanbe;
    }

    public void setPanjshanbe(String panjshanbe) {
        this.panjshanbe = panjshanbe;
    }

    public void setJome(String jome) {
        this.jome = jome;
    }

    public DaysOfWeek(String shanbe, String yekshanbe, String doshanbe, String seshanbe, String chaharshanbe, String panjshanbe, String jome) {
        this.shanbe = shanbe;
        this.yekshanbe = yekshanbe;
        this.doshanbe = doshanbe;
        this.seshanbe = seshanbe;
        this.chaharshanbe = chaharshanbe;
        this.panjshanbe = panjshanbe;
        this.jome = jome;
    }
}
